package helper;

import java.util.Comparator;
import java.util.List;

/**
 *
 * @author Katy
 */
public class MyBinarySearch {
    
     public static <T> T getObjectWithBinarySearch(List<? extends T> l, T key, Comparator<? super T> c) {
        int low = 0;
        int high = l.size()-1;

        while (low <= high) {
            int mid = (low + high) >>> 1;
            T midVal = l.get(mid);
            int cmp = c.compare(midVal, key);

            if (cmp < 0)
                low = mid + 1;
            else if (cmp > 0)
                high = mid - 1;
            else
                return midVal; // key found
        }
        return null;  // key not found
    }
}
